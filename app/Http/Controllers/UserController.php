     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);

        if($user === null){
            return response()->json(['error' => 'Not Found User'])->setStatusCode(400);
        }

        $this->validate($request, [
            'name'  => 'required',
            'email' => 'required',
        ]);

        $user->update($request->all());

        return response()->json(['result' => 'ok']);

